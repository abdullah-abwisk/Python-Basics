''' escape sequences '''

# print a new line
print("Hello\nWorld!") # Hello
                       # World!
# print a tab
print("Hello\tWorld!") # Hello   World!

# print a backslash
print("Hello\\World!") # Hello\World!

# print a single quote
print("Hello\'World!") # Hello'World!

# print a double quote
print("Hello\"World!") # Hello"World!

# separator character
print("Hello", 6, 7, sep="~", end="!") # Hello~6~7!