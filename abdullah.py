def greeting(name):
    print("Hello, " + name)
    print("How are you today?")
    print("I hope you are doing well!")
    
def farewell(name):
    print("Goodbye, " + name)
    print("I hope to see you again soon!")
    print("Have a great day!")
    
# This is the main entry point of the program
if __name__ == "__main__":
    greeting("Abdullah")
    farewell("Abdullah")